package com.basics.arrays;

public class Swap {

	public static void main(String[] args) {
		int a=10;
		int b=20;
		int temp=0;
		 
		
		temp=a;
		a=b;
		b=temp;
		System.out.println(a);
		System.out.println(b);
		
		
		int c=10;
		int d=20;
		
		c=c+d;
		d=c-d;
		c=c-d;
		
		System.out.println(c);
		System.out.println(d);

	}

}
